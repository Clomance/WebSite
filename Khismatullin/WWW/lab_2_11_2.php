<?php
    $n=rand(1,1000);
    $m=rand(1000,5000);

    print("Интервал - [$n;$m] <p>");

    for ($x=$n; $x<$m; $x++){
        $r=intval(floor(pow($x, 1/3)),10); // Число, куб которого ближе всего к исходному

        for ($a=1; $a<$r; $a++){
            for ($b=$a; $b<$r; $b++){
                $t=$a*$a*$a+$b*$b*$b;
                if ($x==$t){
                    print("Исходное натуральное число - $x <p>");
                    continue;
                }
            }
        }
    }
?>